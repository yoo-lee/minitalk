#include "client.h"

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}

void	error(char *str)
{
	ft_putstr(str);
	exit(0);
}
